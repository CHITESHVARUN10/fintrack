function fmtINR(n) {
  if (!isFinite(n)) return '';
  return Math.round(n).toLocaleString('en-IN');
}

function buildSlabLabels(bounds) {
  const labels = [];
  let prev = 0;
  for (let i = 0; i < bounds.length; i++) {
    const upper = bounds[i];
    if (upper === Infinity) {
      labels.push(`Above ₹${fmtINR(prev)}`);
    } else if (prev === 0) {
      labels.push(`Up to ₹${fmtINR(upper)}`);
    } else {
      labels.push(`₹${fmtINR(prev + 1)} – ₹${fmtINR(upper)}`);
    }
    prev = upper;
  }
  return labels;
}
const SLAB_LABELS = {
  Old: buildSlabLabels(SLAB_BOUNDS.Old),
  New: buildSlabLabels(SLAB_BOUNDS.New),
};

// Return the surcharge bracket (lower bound + rate) the income falls into.
function surchargeBracket(totalIncome, regime) {
  let lower = 0;
  for (const t of _cfg.SURCHARGE_TIERS) {
    if (t.limit === Infinity) {
      return { lower, rate: regime === 'New' ? _cfg.NEW_SURCHARGE_CAP : _cfg.OLD_SURCHARGE_TOP };
    }
    if (totalIncome <= t.limit) return { lower, rate: t.rate };
    lower = t.limit;
  }
  return { lower, rate: 0 };
}

/**
 * Slab-wise tax on a given taxable income. Returns each band with the income
 * falling inside it, the rate, and the tax generated by that band, plus the
 * summed income tax (before rebate/surcharge).
 */
function computeSlabTax(taxable, regime) {
  const bounds = SLAB_BOUNDS[regime];
  const rates = SLAB_RATES[regime];
  const labels = SLAB_LABELS[regime];
  const t = Math.max(0, taxable);
  let prev = 0;
  let incomeTax = 0;
  const slabs = [];
  for (let i = 0; i < bounds.length; i++) {
    const upper = bounds[i];
    const rate = rates[i];
    const incomeInBand = Math.max(0, Math.min(t, upper) - prev);
    const tax = Math.round(incomeInBand * rate);
    slabs.push({
      label: labels[i],
      lower: prev === 0 ? 0 : prev + 1,
      upper: upper === Infinity ? null : upper,
      rate,
      incomeInBand,
      tax,
    });
    incomeTax += tax;
    prev = upper;
    if (t <= upper) break;
  }
  return { slabs, incomeTax };
}

/** Section 87A rebate with marginal relief (FY 2025-26). */
function computeRebate(taxable, regime, incomeTaxBeforeRebate) {
  const { threshold, max } = REBATE[regime];
  const eligibleRebate = Math.min(incomeTaxBeforeRebate, max);
  if (taxable <= threshold) return eligibleRebate;
  // Marginal relief: rebate is reduced by the amount income exceeds the
  // threshold, so the taxpayer does not lose the whole rebate for a small
  // excess. Tax payable then ≈ only the excess over the threshold.
  const reduced = eligibleRebate - (taxable - threshold);
  return Math.max(0, reduced);
}

/** Surcharge on income tax (after rebate) with marginal relief. */
function computeSurcharge(totalIncome, incomeTaxAfterRebate, regime) {
  const { lower: threshold, rate } = surchargeBracket(totalIncome, regime);
  if (rate === 0) return 0;
  const surcharge = Math.round(incomeTaxAfterRebate * rate);

  // Marginal relief: crossing a surcharge tier must not make the total tax
  // (income tax + surcharge) jump beyond the tax on the threshold income PLUS
  // the excess income above that threshold. The threshold is a gross income,
  // so we subtract the standard deduction before computing its slab tax.
  const thresholdTaxable = Math.max(0, threshold - _cfg.STANDARD_DEDUCTION[regime]);
  const taxAtThreshold = computeSlabTax(thresholdTaxable, regime).incomeTax;
  const maxPreCess = taxAtThreshold + (totalIncome - threshold);
  const currentPreCess = incomeTaxAfterRebate + surcharge;
  if (currentPreCess > maxPreCess) {
    return Math.max(0, Math.round(maxPreCess - incomeTaxAfterRebate));
  }
  return surcharge;
}

// Statutory HRA exemption (Old regime only). The exemption is the LEAST of:
//   (a) actual HRA received,
//   (b) 10% of basic salary,
//   (c) rent paid minus 10% of basic salary.
// If we lack the basic salary or the rent paid, we CANNOT compute it, so it
// returns 0 — we never guess an HRA exemption (the Form 16 may not state rent
// paid, which is exactly the "not enough information" case from the audit).
function computeHraExemption({ basicSalary = 0, hra = 0, rentPaid = null, regime }) {
  if (regime !== 'Old' || !basicSalary || rentPaid == null) return 0
  const tenPctBasic = 0.1 * basicSalary
  const rentLessTenPct = Math.max(0, rentPaid - tenPctBasic)
  return Math.max(0, Math.min(hra || 0, tenPctBasic, rentLessTenPct))
}

