/**
 * Build the per-regime deduction plan. Each item shows whether the deduction is
 * allowed under the regime, the statutory cap, the amount the user actually
 * has (userAmount), the amount claimed, and the remaining eligible headroom.
 * Disallowed items are kept (claimed 0) so the UI can explain what is excluded.
 */
function buildDeductionPlan(regime, input, sources = {}) {
  const sd = STANDARD_DEDUCTION[regime];
  const plan = [];

  const push = (key, userAmount, source) => {
    const meta = DED_META[key];
    const allowed = meta.allowed[regime];
    const max = meta.max === 'sd' ? sd : meta.max;
    const cap = allowed ? (max == null ? Infinity : max) : 0;
    const claimed = allowed ? Math.min(userAmount, cap) : 0;
    const remaining = allowed && max != null ? Math.max(0, max - claimed) : allowed && max == null ? null : 0;
    plan.push({
      key,
      label: meta.label,
      allowed,
      disallowed: !allowed,
      maxLimit: max == null ? null : max,
      userAmount: round(userAmount),
      claimed: round(claimed),
      remaining, // null = unlimited, 0 = none, number = headroom
      qualifying: allowed && userAmount > 0,
      // Provenance: where this claimed amount came from (Form 16, the user's
      // declared records, or both). Null when nothing was claimed.
      source: source ?? (allowed && userAmount > 0 ? 'Declared' : null),
      note: meta.note,
    });
  };

  push('standardDeduction', sd, sources.standardDeduction);
  push('hra', input.hra || 0, sources.hra);
  push('lta', input.lta || 0, sources.lta);
  push('section80C', input.section80C || 0, sources.section80C);
  push('section80CCD1B', input.section80CCD || 0, sources.section80CCD1B);
  push('section80CCD2', input.section80CCD || 0, sources.section80CCD2);
  push('section80D', input.section80D || 0, sources.section80D);
  push('section80E', input.section80E || 0, sources.section80E);
  push('section80G', input.section80G || 0, sources.section80G);
  push('section24', input.section24 || 0, sources.section24);

  return plan;
}

/**
 * Full FY 2025-26 tax liability for one regime, following the statutory
 * waterfall. Returns every intermediate value so the UI can render (and expand)
 * each step.
 */
function computeLiability({
  grossSalary,
  regime,
  hra = 0,
  lta = 0,
  deductions = {},
  tdsDeducted = 0,
  advanceTax = 0,
  selfAssessmentTax = 0,
  sources = {},
}) {
  const sd = STANDARD_DEDUCTION[regime];

  // Salary exemptions are allowed only under the Old regime.
  const salaryExemptions = {
    hra: regime === 'Old' ? Math.max(0, hra || 0) : 0,
    lta: regime === 'Old' ? Math.max(0, lta || 0) : 0,
  };

  const incomeFromSalary = Math.max(0, (grossSalary || 0) - salaryExemptions.hra - salaryExemptions.lta);
  const incomeFromOtherSources = 0; // not captured in the app's data model
  const grossTotalIncome = Math.max(0, incomeFromSalary + incomeFromOtherSources);

  const plan = buildDeductionPlan(
    regime,
    {
      hra: salaryExemptions.hra,
      lta: salaryExemptions.lta,
      section80C: deductions.section80C || 0,
      section80CCD: deductions.section80CCD || 0,
      section80D: deductions.section80D || 0,
      section80E: deductions.section80E || 0,
      section80G: deductions.section80G || 0,
      section24: deductions.section24 || 0,
    },
    sources,
  );

  const totalDeductions = plan.reduce((s, d) => s + d.claimed, 0);
  const taxableIncome = Math.max(0, grossTotalIncome - totalDeductions);

  const slab = computeSlabTax(taxableIncome, regime);
  const incomeTaxBeforeRebate = slab.incomeTax;
  const rebate = computeRebate(taxableIncome, regime, incomeTaxBeforeRebate);
  const incomeTaxAfterRebate = Math.max(0, incomeTaxBeforeRebate - rebate);
  const surcharge = computeSurcharge(grossTotalIncome, incomeTaxAfterRebate, regime);
  const cess = Math.round((incomeTaxAfterRebate + surcharge) * _cfg.CESS);
  const finalTax = incomeTaxAfterRebate + surcharge + cess;

  const totalPaid = (tdsDeducted || 0) + (advanceTax || 0) + (selfAssessmentTax || 0);
  const balance = finalTax - totalPaid;
  const refund = balance < 0 ? Math.round(-balance) : 0;
  const taxPayable = balance > 0 ? Math.round(balance) : 0;

  return {
    regime,
    grossIncome: grossSalary || 0,
    salaryExemptions,
    incomeFromSalary,
    incomeFromOtherSources,
    grossTotalIncome,
    deductions: plan,
    totalDeductions,
    taxableIncome,
    slabs: slab.slabs,
    incomeTaxBeforeRebate,
    rebate,
    incomeTaxAfterRebate,
    surcharge,
    cess,
    finalTax,
    tdsDeducted: tdsDeducted || 0,
    advanceTax: advanceTax || 0,
    selfAssessmentTax: selfAssessmentTax || 0,
    totalPaid,
    refund,
    taxPayable,
  };
}

/**
 * Compute BOTH regimes from the SAME gross salary + SAME aggregated deductions,
 * so the recommendation compares like-for-like final tax liabilities.
 */
function computeRegimeTaxes(grossSalary, agg, extra = {}, sources = {}) {
  const base = {
    section80C: agg.section80C || 0,
    section80CCD: agg.section80CCD || 0,
    section80D: agg.section80D || 0,
    section80E: agg.section80E || 0,
    section80G: agg.section80G || 0,
    section24: agg.section24 || 0,
  };
  const old = computeLiability({ grossSalary, regime: 'Old', hra: extra.hra, lta: extra.lta, deductions: base, tdsDeducted: extra.tdsDeducted || 0, sources });
  const newR = computeLiability({ grossSalary, regime: 'New', hra: extra.hra, lta: extra.lta, deductions: base, tdsDeducted: extra.tdsDeducted || 0, sources });
  return { old, new: newR };
}

// Backwards-compatible thin wrapper used by a few legacy call sites.
function calculateTax(grossIncome, deductions, regime) {
  const r = computeLiability({
    grossSalary: grossIncome,
    regime,
    deductions: { section80C: deductions },
  });
  return {
    taxableIncome: r.taxableIncome,
    taxBeforeCess: r.incomeTaxAfterRebate,
    cess: r.cess,
    totalTax: r.finalTax,
  };
}

function annualize(amount, frequency) {
  if (!amount) return 0;
  if (frequency === 'One-time') return amount;
  const div = FREQ_MONTHS[frequency] || 12;
  return amount * (12 / div);
}

/**
 * Aggregate deductible amounts from all financial modules for a user.
 * Values are estimates suitable for a quick comparison/recommendation.
 */
async function aggregateDeductions(userId) {
  const [investments, insurances, loans] = await Promise.all([
    Investment.find({ memberId: userId }),
    Insurance.find({ memberId: userId }),
    EMILoan.find({ memberId: userId }),
  ]);

  let section80C = 0;
  let section80CCD = 0;
  let section80D = 0;
  let section80E = 0;
  let section80G = 0;
  let section24 = 0;

  for (const inv of investments) {
    if (inv.fundCategory === 'ELSS' || inv.assetType === 'PPF') section80C += investedValue(inv);
    if (inv.assetType === 'NPS') section80CCD += investedValue(inv);
  }

  for (const ins of insurances) {
    if (ins.insuranceType === 'Health') {
      section80D += annualize(ins.premiumAmount, ins.premiumFrequency);
    }
  }

  for (const loan of loans) {
    if (loan.loanType === 'Home' && loan.interestRate && loan.principalAmount) {
      const annualInterest = Math.min((loan.interestRate / 100) * loan.principalAmount, 200000);
      section24 += annualInterest;
      const principalRepaid = Math.max(0, (loan.emiAmount || 0) * 12 - annualInterest);
      section80C += principalRepaid;
    }
  }

  // Section 80C has a hard cap — read from config.
  section80C = Math.min(section80C, _cfg.DEDUCTION_CAPS.SECTION_80C_GROUP);

  const totalDeductions =
    section80C + section80CCD + section80D + section80E + section80G + section24;

  return {
    section80C: round(section80C),
    section80CCD: round(section80CCD),
    section80D: round(section80D),
    section80E: round(section80E),
    section80G: round(section80G),
    section24: round(section24),
    totalDeductions: round(totalDeductions),
  };
}

